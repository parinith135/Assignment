/*5. Implement a JAVA program to find the square root of the number. (import MATH,can use sqrt)*/
import java.lang.*;
class Squareroot{
	public static void main(String args[]) {
		int num = 25;
		double S;
		S = Math.sqrt(num);
		System.out.println("Squareroot of given number is " + S);
	}
}